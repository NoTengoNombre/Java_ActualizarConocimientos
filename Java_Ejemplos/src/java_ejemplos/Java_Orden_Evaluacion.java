public class Java_Orden_Evaluacion {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        int bb;
        int bb1;
        int aa = 5;
        int cc = 11;
        System.out.printf("\u001B[33m" + aa + " <- aa \n");
        System.out.println("\u001B[32m" + cc + " <- cc ");

        bb = aa - cc;
        System.out.println(bb + " <- bb = aa - cc ");
        bb1 = cc - aa;
        System.out.println(bb1 + " <- bb = cc - aa ");

    }

}
