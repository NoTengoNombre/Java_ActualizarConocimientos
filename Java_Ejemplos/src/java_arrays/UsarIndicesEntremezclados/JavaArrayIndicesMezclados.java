package java_arrays.UsarIndicesEntremezclados;

public class JavaArrayIndicesMezclados {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; i < 10 && j < 10; j++) {
                System.out.println(" i : " + i + " - j : " + j);
            }
        }
    }

}
