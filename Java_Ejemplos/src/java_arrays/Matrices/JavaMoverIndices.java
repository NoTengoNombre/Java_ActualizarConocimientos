package java_arrays.Matrices;

public class JavaMoverIndices {

    public static void main(String[] args) {

        for (int f = 0; f < 10; f++) {
            for (int c = f; c < 10; c++) {
                System.out.println("f : " + f + " c : " + c);
            }
            System.out.println("");
        }
    }
}
