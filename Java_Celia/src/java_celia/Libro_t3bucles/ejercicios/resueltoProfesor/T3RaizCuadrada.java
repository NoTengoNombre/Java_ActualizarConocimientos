package java_celia.Libro_t3bucles.ejercicios.resueltoProfesor;

public class T3RaizCuadrada {

    public double calcularRaizCuadrada(double n) {
        if (n >= 0) {
            return Math.sqrt(n);
        } else {
            return -1;
        }
    }
}
